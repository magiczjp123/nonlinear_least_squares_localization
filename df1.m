function [ R ] = df1(X, CASE)
%UNTITLED3 ????????????
%   ????????

R = zeros(1,size(X,2));

if CASE == 0;

    R(1,1) = -X(1)/(sqrt(X(1)^2 + X(2)^2));
    R(1,2) = -X(2)/(sqrt(X(1)^2 + X(2)^2));
    
end

if CASE == 1;
    

    R(1,1) = (X(1) - X(4))/(sqrt((X(4) - X(1))^2 + (X(5) - X(2))^2));
    R(1,2) = (X(2) - X(5))/(sqrt((X(4) - X(1))^2 + (X(5) - X(2))^2));
    R(1,4) = (X(4) - X(1))/(sqrt((X(4) - X(1))^2 + (X(5) - X(2))^2));
    R(1,5) = (X(5) - X(2))/(sqrt((X(4) - X(1))^2 + (X(5) - X(2))^2));
end

end

